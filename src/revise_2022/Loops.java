
package revise_2022;

public class Loops {

	public static void main(String[] args) { // TODO Auto-generated method stub


		int i=1;
		while (i<10) {
			System.out.println("i is lessthan "+i);
			i++;
		}

		for(int j=1;j<10;j++) {
			System.out.println(j);
		}

		for (int k=10;k>=1;k--) {
			System.out.println(k);
		}

	}

}
